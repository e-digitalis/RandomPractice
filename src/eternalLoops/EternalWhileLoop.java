public class EternalWhileLoop {
   	public static void main(String[] args) {

		/*
		 * A while loop is actually just a conditional that repeats itself as
		 * long as the condition stays true. It looks a lot like an if
		 * statement.
		 * 
		 * Read more: http://www.java-made-easy.com/for-loop.html#ixzz4NijGlBTp
		 */

		int num=3;
		while (num == 3) {
		System.out.println(num);
		}

	} 
}
